"""
    #Класс для управления сюжетными частями игры.
"""
class GameStory:
    def __init__(self):
        # Список частей сюжета
        self.story_parts = [
            {
                "title": "Начало",
                "text": "2150 год. Вторжение Кибернетиков\n\n"
                       "Человечество колонизировало множество планет, достигнув расцвета космической эры. "
                       "Но внезапно из глубин Вселенной появилась угроза — Кибернетики, гибридная раса, "
                       "сочетающая органику и машины. Они начали захватывать колонии, превращая их в свои базы, "
                       "и теперь лишь немногие оплоты людей остаются свободными."
            },
            {
                "title": "Миссия",
                "text": "Вы - пилот элитного космического истребителя 'Феникс', одного из самых современных "
                       "и мощных кораблей человечества. Ваша первая задача - прорваться через передовую линию "
                       "обороны врага. Это будет первым испытанием в вашей миссии по спасению человечества."
            }
        ]
        self.current_part = 0

    def get_current_story(self):
        """Возвращает текст текущей части сюжета."""
        return self.story_parts[self.current_part]["text"]

    def next_story(self):
        """Переходит к следующей части сюжета. Возвращает True, если есть следующая часть."""
        if self.current_part < len(self.story_parts) - 1:
            self.current_part += 1
            return True
        return False

    def has_next_story(self):
        """Проверяет, есть ли следующая часть сюжета."""
        return self.current_part < len(self.story_parts) - 1

    def reset_story(self):
        """Сбрасывает прогресс сюжета на начало."""
        self.current_part = 0 